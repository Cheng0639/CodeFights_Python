def sudoku(grid):
    def line(i):
        return len(set(grid[i])) != 9

    def col(i):
        return len(set([grid[x][i]for x in range(9)])) != 9

    def matrix(i, j):
        return len(set([grid[3 * i + x][3 * j + y] for x in range(0, 3) for y in range(0, 3)])) != 9

    checkLine = sum([line(i) for i in range(9)]) == 0
    checkCol = sum([col(i) for i in range(9)]) == 0
    checkMatrix = sum([matrix(i, j) for i in range(0, 3)
                       for j in range(0, 3)]) == 0

    return checkLine and checkCol and checkMatrix


