def removeTasks(k, toDo):
    del toDo[k - 1::k]
    return toDo


print(removeTasks(3, [1237, 2847, 27485, 2947, 1, 247, 374827, 22]) == [
      1237, 2847, 2947, 1, 374827, 22])
print(removeTasks(1,  [1237, 2847, 27485, 2947, 1, 247, 374827, 22]) == [])
print(removeTasks(10, [1237]) == [1237])
print(removeTasks(3, [552, 987, 422, 309, 222, 949, 448, 524, 542, 562, 105, 210, 887, 762, 750,
                      239, 738, 920, 764]) == [552, 987, 309, 222, 448, 524, 562, 105, 887, 762, 239, 738, 764])
print(removeTasks(2, [82614, 34200, 25887, 53226, 32161, 9495, 49341, 81259, 83693,
                      30921, 46944, 8450, 63590, 97745]) == [82614, 25887, 32161, 49341, 83693, 46944, 63590])
